# Prompt the user to enter a word
word = input("Enter a Word :")
# and assign it to the user_word variable.
user_word = word.upper()
for letter in user_word:
    # Complete the body of the for loop.
    if letter == "A" :
        continue
    elif letter == "I" :
        continue
    elif letter == "U" :
        continue
    elif letter == "E" :
        continue
    elif letter == "O" :
        continue
    else :
        print(letter)
